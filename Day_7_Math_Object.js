/*
Explanation of Code:

1. console.log(Math.PI);
   - Prints the value of PI (≈ 3.14159) from the Math object.

2. let x = 1.5;

3. console.log(Math.floor(x));
   - Rounds x down to the nearest integer → 1.

4. console.log(Math.round(x));
   - Rounds x to the nearest integer → 2 (since 1.5 rounds up).

5. console.log(Math.ceil(x));
   - Rounds x up to the nearest integer → 2.

6. console.log(Math.sqrt(x));
   - Returns the square root of x → √1.5 ≈ 1.2247.

7. console.log(Math.abs(x));
   - Returns the absolute value of x (distance from zero) → 1.5.

8. a = 1; b = 5; c = 10;

9. console.log(Math.max(a, b, c));
   - Returns the largest value among a, b, and c → 10.
*/
